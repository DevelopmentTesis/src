package com.android.hotoffer.to;

public class Recordar {

	private String user;
	private String pass;

	public Recordar() {

	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}

}
